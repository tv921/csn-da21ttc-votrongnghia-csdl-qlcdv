    <h2>Lễ ký kết Biên bản ghi nhớ thực hiện Chương trình “75 nghìn sáng kiến, vượt khó, phát triển”.</h2>
    <img src="hinh/tvu.jpg" alt="">
    <p>(TVU)-Sáng ngày 24/3/2021, Ban Giám Hiệu Trường Đại học Trà Vinh phối hợp Công đoàn cơ sở (CĐCS) của Trường tổ chức Lễ ký kết Biên bản ghi nhớ thực hiện Chương trình “75 nghìn sáng kiến, vượt khó, phát triển” tại Hội trường E21.105 của Nhà trường.</p>
    <p>Chương trình do Ban Thường vụ Liên đoàn lao động (LĐLĐ) tỉnh Trà Vinh phát động nhằm tổ chức các hoạt động hưởng ứng Tháng Công nhân và Tháng hành động, tạo sự lan tỏa sâu rộng về việc hưởng ứng chương trình của Tổng LĐLĐ Việt Nam. Ban Thường vụ LĐLĐ tỉnh chọn CĐCS Trường Đại học Trà Vinh làm điểm chỉ đạo (của khối các Công đoàn cơ sở ở cơ quan Nhà nước, đơn vị sự nghiệp công lập) trong việc tổ chức Lễ ký kết Biên bản ghi nhớ thực hiện chương trình “75 nghìn sáng kiến, vượt khó, phát triển”.</p>

    <p>Phát biểu ý kiến tại buổi lễ, Chủ tịch LĐLĐ tỉnh Thạch Thị Thu Hà kêu gọi: “Đoàn viên và công nhân viên chức lao động phát huy tinh thần thi đua, lao động sáng tạo, sáng kiến góp phần cùng cơ quan, đơn vị, doanh nghiệp vượt qua khó khăn, hoàn thành các chỉ tiêu, nhiệm vụ công tác, sản xuất kinh doanh từng đơn vị, từng doanh nghiệp góp phần thực hiện thắng lợi mục tiêu, nhiệm vụ phát triển kinh tế, xã hội của tỉnh nhà. Với chỉ tiêu phấn đấu thực hiện 1000 sáng kiến, cải tiến kỹ thuật, giải pháp hữu ích, hợp lí hóa sản xuất, đề tài nghiên cứu khoa học… và đề ra chỉ tiêu cụ thể bình quân mỗi CĐCS có ít nhất 1 sáng kiến.‎</p>
    <p>Trường Đại học Trà Vinh là một trong những đơn vị dẫn đầu của tỉnh trong phong trào thi đua “Lao động giỏi, lao động sáng tạo” được Ban Thường vụ LĐLĐ tỉnh chọn CĐCS Trường Đại học Trà Vinh làm điểm chỉ đạo tổ chức Lễ ký kết Biên bản ghi nhớ thực hiện Chương trình để các LĐLĐ huyện, thị xã, thành phố, Công đoàn ngành, CĐCS trực thuộc nghiên cứu, học tập kinh nghiệm. ”</p>
    <p>Phó Hiệu trưởng Nhà trường – Thạch Thị Dân chia sẻ: “Năm 2021 có ý nghĩa đặc biệt quan trọng đối với trường, 15 năm Đại học Trà Vinh – 20 năm mô hình cao đẳng cộng đồng, trải qua chặng đường ấy, từ những khó khăn ban đầu, tập thể Đảng ủy, Ban Giám Hiệu, các đoàn thể và giảng viên, sinh viên Nhà trường chung sức chung lòng bắt tay vào công cuộc xây dựng và phát triển. Tiêu biểu là sự tham gia tích cực phong trào thi đua “Lao động giỏi, lao động sáng tạo”. Chỉ tiêu phấn đấu trong năm 2021 có trên 300 sáng kiến từ các đơn vị được triển khai thực hiện và đạt hiệu quả”.</p>
    <p>Buổi lễ đã khen thưởng cho 4 đoàn viên có sáng kiến tiêu biểu bao gồm: Đồng chí Nguyễn Huy Thông với sáng kiến “Phát huy hiệu quả các nguồn lực nhằm thực hiện tốt công tác phòng chống dịch bệnh trong nhà trường song song với việc đảm bảo chất lượng dạy và học”, Đồng chí Nguyễn Văn Long với sáng kiến: “Ứng dụng công nghệ thông tin trong xây dựng câu hỏi điện tử Hội thi tìm hiểu tư tưởng, đạo đức, phong cách Hồ Chí Minh”, Đồng chí Phạm Vũ Bằng với sáng kiến: “Ứng dụng kỹ thuật ngập khô xen kẽ trong canh tác lúa ở Đồng bằng sông Cửu Long”, Đồng chí Đặng Vũ Hoàng với sáng kiến: “Chế tạo máy tách vỏ trái dừa tại tỉnh Trà Vinh phục vụ sản xuất tơ xơ dừa”. Cùng với đó, Ban Thường vụ LĐLĐ tỉnh quyết định tặng 1500 suất quà cho 1500 đoàn viên công đoàn, công nhân lao động có hoàn cảnh khó khăn, trong đó có 20 đoàn viên CĐCS trường Đại học Trà Vinh.</p>
    <p>Đây là đơn vị đầu tiên của tỉnh tổ chức ký kết, phát động thực hiện chương trình. Theo đó, hai bên phối hợp tuyên truyền, vận động đoàn viên, người lao động tích cực tham gia chương trình bằng nhiều hình thức; tổ chức biểu dương, tôn vinh, khen thưởng các tập thể, cá nhân tích cực tham gia, hưởng ứng, có nhiều sáng kiến mang lại hiệu quả cao trong thời gian triển khai thực hiện chương trình.</p>

